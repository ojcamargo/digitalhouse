<?php

abstract class Ave
{
    protected $asas;
    protected $penas;
    protected $bico;

    public function getAsas()
    {
        return $this->asas;
    }

    public function getPenas()
    {
        return $this->penas;
    }

    public function getBico()
    {
        return $this->bico;
    }
}
